<?php
namespace Models;

class Product
{
    protected $name;
    protected $price;
    public function __construct($name = null, $price = 0 )
    {
        $this->name = $name;
        $this->price = $price;
    }

    /**
     * @return null
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param null $name
     */
    public function setName($name): void
    {
        $this->name = $name;
    }

    /**
     * @return int
     */
    public function getPrice(): int
    {
        return $this->price;
    }

    /**
     * @param int $price
     */
    public function setPrice(int $price): void
    {
        $this->price = $price;
    }

}